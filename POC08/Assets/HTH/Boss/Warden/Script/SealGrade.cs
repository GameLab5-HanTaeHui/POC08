//
// [등급별 정책 — README v2 봉인 집행 등급 3단계]
//   Normal : 일반/엘리트 몬스터 → 슬로우 없음, 전투 템포 유지
//   Part   : 보스 부위 (팔 등) → 짧은 슬로우, 강한 이펙트
//   Core   : 보스 코어/최종   → 풀 불릿타임, 카메라 줌, 강한 연출
//
// [namespace] SEAL
// ============================================================

namespace SEAL
{
    /// <summary>
    /// 봉인 집행 등급.
    /// SealableComponent 에서 Inspector 로 설정.
    /// SealExecutor 에서 등급에 따라 슬로우/연출 분기.
    /// </summary>
    public enum SealGrade
    {
        /// <summary>
        /// 1단계 일반 봉인.
        /// 대상: 일반 몬스터, 엘리트 몬스터.
        /// 슬로우 없음. 전투 흐름을 끊지 않는다.
        /// 연쇄 봉인 가능.
        /// </summary>
        Normal,

        /// <summary>
        /// 2단계 부위 봉인.
        /// 대상: 미니보스 / 보스 부위 (팔, 다리 등).
        /// 짧은 슬로우 + 강한 이펙트.
        /// 전투 중 여러 번 발생하는 공략 행동.
        /// </summary>
        Part,

        /// <summary>
        /// 3단계 코어 봉인.
        /// 대상: 보스 코어, 최종 코어.
        /// 풀 불릿타임 + 카메라 줌 + 강한 사운드 + 자물쇠/사슬 이펙트.
        /// 보스를 확실히 봉인하는 연출적 의미.
        /// </summary>
        Core
    }
}